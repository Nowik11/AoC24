package Zadania;

public class zad14_wektory {
    private int vx;
    private int vy;
    private int px;
    private int py;

    public zad14_wektory(int vx, int vy, int px, int py) {
        this.vx = vx;
        this.vy = vy;
        this.px = px;
        this.py = py;
    }

    public int getVx() {
        return vx;
    }

    public void setVx(int vx) {
        this.vx = vx;
    }

    public int getVy() {
        return vy;
    }

    public void setVy(int vy) {
        this.vy = vy;
    }

    public int getPx() {
        return px;
    }

    public void setPx(int px) {
        this.px = px;
    }

    public int getPy() {
        return py;
    }

    public void setPy(int py) {
        this.py = py;
    }
}
